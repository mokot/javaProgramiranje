public class Tip {
    public static void main(String[] args) {
        int stevilo; // Deklaracija: 
        stevilo = 42; // Inicializacija:

        int spremenljivkaIme = 4 * 8 + 20 / 2; // spremenljivka = izraz;

        boolean logicnaVrednost = true; // boolean
        byte steviloByte = 124;  // byte
        short steviloShort = -200; // short
        int steviloInt = -4250000; // int
        long steviloLong = 42332200000L; // long
        float steviloFloat = -42.38f; // float
        double steviloDouble = 832.48329; // double
        char znak = 'a'; // char
        char znakInt = 65; // char (int)
        String nizZnakov = "Programiranje je zabavno."; // String

        System.out.println(stevilo);
        System.out.println(spremenljivkaIme);
        System.out.println();
        System.out.println(logicnaVrednost);
        System.out.println(steviloByte);
        System.out.println(steviloShort);
        System.out.println(steviloInt);
        System.out.println(steviloLong);
        System.out.println(steviloFloat);
        System.out.println(steviloDouble);
        System.out.println(znak);
        System.out.println(znakInt);
        System.out.println(nizZnakov);
    }
}