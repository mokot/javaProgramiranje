public class EnojniOperator {
    public static void main(String[] args) {

        int stevilo = 42;

        System.out.println("+stevilo = " + +stevilo);

        System.out.println("-stevilo = " + -stevilo);

        System.out.println("++stevilo = " + ++stevilo);

        System.out.println("--stevilo = " + --stevilo);
    }
}